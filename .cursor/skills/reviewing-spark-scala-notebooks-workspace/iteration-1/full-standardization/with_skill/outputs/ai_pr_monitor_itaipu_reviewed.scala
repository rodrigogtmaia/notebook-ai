// MAGIC **Main assumptions**:
// MAGIC - `action === "closed"` and `is_merged === true` define the merged-PR population; `merged_at >= 2025-01-01` bounds the OKR window.
// MAGIC - Rapidash PRs are identified by `repo_name === "itaipu"` and `description` containing `Generated by Rapidash`.
// MAGIC - Warp Pipe PRs use the same repo plus title/description patterns documented in the filter block (excludes infra-only mentions).
// MAGIC - All PRs in `cross-segment-ai-tools` are treated as AI-assisted with tool label `cross-segment-ai-tools`.
// MAGIC - Estimated output counts for Rapidash use description parsing heuristics; they are approximate, not ground truth.

