// DBTITLE 1,Check required columns on filtered base
val requiredColumnsForPipeline = Seq(
  "action",
  "is_merged",
  "merged_at",
  "created_at",
  "repo_name",
  "description",
  "title",
  "pr_number",
  "author",
  "added_lines",
  "deleted_lines",
  "changed_files"
)

val missingColumns = requiredColumnsForPipeline.filterNot(mergedPullRequestEvents.columns.contains)
require(missingColumns.isEmpty, s"Missing required columns after merged-pr filter: ${missingColumns.mkString(", ")}")

// COMMAND ----------

// DBTITLE 1,Validate filter impact and merged_at bounds
val mergedClosedRowCount = mergedPullRequestEvents.count()
val aiAssistedRowCount = aiAssistedPullRequests.count()

println(s"Rows after merged + date filter: $mergedClosedRowCount")
println(s"Rows after AI repo/signature filter: $aiAssistedRowCount")

val mergedAtMinMax = mergedPullRequestEvents.agg(min("merged_at").alias("min_merged_at"), max("merged_at").alias("max_merged_at")).head()
println(s"merged_at min (merged scope): ${mergedAtMinMax.getAs[Any]("min_merged_at")}")
println(s"merged_at max (merged scope): ${mergedAtMinMax.getAs[Any]("max_merged_at")}")

val nullMergedAtInScope = mergedPullRequestEvents.filter(col("merged_at").isNull).count()
require(nullMergedAtInScope == 0, s"Unexpected null merged_at in merged scope: $nullMergedAtInScope rows")

// COMMAND ----------

// DBTITLE 1,Check duplicate grain repo_name and pr_number in AI slice
val duplicatePrKeys = aiAssistedPullRequests
  .groupBy("repo_name", "pr_number")
  .count()
  .filter(col("count") > 1)

val duplicatePrKeyCount = duplicatePrKeys.count()
println(s"Duplicate (repo_name, pr_number) groups in AI slice: $duplicatePrKeyCount")
if (duplicatePrKeyCount > 0) {
  display(duplicatePrKeys.limit(100))
}

// COMMAND ----------

// DBTITLE 1,Validate aggregation safety for monthly PR counts
val distinctPrInAiSlice = aiAssistedPullRequests.select("pr_number").distinct().count()
val sumMonthlyPrsAcrossTools = monthlyVolume.agg(sum("total_prs").alias("sum_distinct_prs")).head().getLong(0)

println(s"Global distinct pr_number in AI slice: $distinctPrInAiSlice")
println(s"Sum of monthly total_prs (all month x ai_tool rows): $sumMonthlyPrsAcrossTools")

require(
  distinctPrInAiSlice == sumMonthlyPrsAcrossTools,
  "Monthly volume total_prs summed across rows should match global distinct pr_number count (each PR falls in exactly one month and one ai_tool)."
)

val sumPrsMergedAcrossAuthorRows = monthlyVolumeByAuthor.agg(sum("prs_merged").alias("s")).head().getLong(0)
println(
  s"Sum of prs_merged across author breakdown rows: $sumPrsMergedAcrossAuthorRows (can exceed global distinct PRs if co-authorship or duplicate keys exist in source)"
)

