// DBTITLE 1,Estimated outputs per PR from description heuristics
// Counting methodology (unchanged):
// - New Rapidash PRs: occurrences of `**Type**:` in the description
// - Update/migration PRs: bullets matching `- **PascalCaseName**`
// - Fallback: max(1, changed_files - 2)
// - cross-segment-ai-tools and Warp Pipe: 1 output per PR
val typeDeclarationCount = size(split(col("description"), "\\*\\*Type\\*\\*:")) - 1

val attributeBulletCount =
  size(expr("filter(split(description, '\n'), x -> x rlike '^- \\\\*\\\\*[A-Z][a-zA-Z0-9]*\\\\*\\\\*')"))

val attributeCountEstimate = greatest(typeDeclarationCount, attributeBulletCount)

val pullRequestsWithEstimatedOutputs = aiAssistedPullRequests
  .withColumn(
    "estimated_outputs",
    when(col("ai_tool") === "cross-segment-ai-tools", lit(1))
      .when(col("ai_tool") === "Warp Pipe", lit(1))
      .when(attributeCountEstimate > 0, attributeCountEstimate)
      .otherwise(greatest(lit(1), col("changed_files") - 2))
  )

// COMMAND ----------

// DBTITLE 1,Monthly PR volume and persist
val monthlyVolume = pullRequestsWithEstimatedOutputs
  .groupBy(date_trunc("month", col("merged_at")).alias("month"), col("ai_tool"))
  .agg(
    countDistinct("pr_number").alias("total_prs"),
    countDistinct("author").alias("unique_authors"),
    sum("added_lines").alias("total_lines_added"),
    sum("deleted_lines").alias("total_lines_deleted"),
    sum("changed_files").alias("total_files_changed")
  )
  .orderBy(col("month").desc, col("ai_tool"))

monthlyVolume.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_volume_mensal"
)

// COMMAND ----------

// DBTITLE 1,Breakdown by author and month and persist
val monthlyVolumeByAuthor = pullRequestsWithEstimatedOutputs
  .groupBy(
    date_trunc("month", col("merged_at")).alias("month"),
    col("ai_tool"),
    col("author")
  )
  .agg(
    countDistinct("pr_number").alias("prs_merged"),
    sum("added_lines").alias("lines_added"),
    sum("deleted_lines").alias("lines_deleted")
  )
  .orderBy(col("month").desc, col("prs_merged").desc)

monthlyVolumeByAuthor.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_por_autor"
)

// COMMAND ----------

// DBTITLE 1,Average time to merge and persist
val monthlyTimeToMerge = pullRequestsWithEstimatedOutputs
  .withColumn(
    "hours_to_merge",
    round((unix_timestamp(col("merged_at")) - unix_timestamp(col("created_at"))) / 3600, 1)
  )
  .groupBy(date_trunc("month", col("merged_at")).alias("month"), col("ai_tool"))
  .agg(
    round(avg("hours_to_merge"), 1).alias("avg_hours_to_merge"),
    min("hours_to_merge").alias("min_hours"),
    max("hours_to_merge").alias("max_hours"),
    countDistinct("pr_number").alias("total_prs")
  )
  .orderBy(col("month").desc, col("ai_tool"))

monthlyTimeToMerge.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_tempo_merge"
)

// COMMAND ----------

// DBTITLE 1,Individual PR list and persist
val pullRequestDetailList = pullRequestsWithEstimatedOutputs
  .withColumn("month", date_format(col("merged_at"), "yyyy-MM"))
  .withColumn(
    "hours_to_merge",
    round((unix_timestamp(col("merged_at")) - unix_timestamp(col("created_at"))) / 3600, 0)
  )
  .select(
    col("month"),
    col("ai_tool"),
    col("repo_name"),
    col("pr_number"),
    col("author"),
    col("title"),
    col("added_lines"),
    col("deleted_lines"),
    col("changed_files"),
    col("hours_to_merge"),
    col("merged_at")
  )
  .orderBy(col("merged_at").desc)

pullRequestDetailList.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_lista_prs"
)

// COMMAND ----------

// DBTITLE 1,Estimated outputs per PR table and persist
val outputsPerPullRequest = pullRequestsWithEstimatedOutputs
  .withColumn("month", date_format(col("merged_at"), "yyyy-MM"))
  .select(
    col("month"),
    col("ai_tool"),
    col("pr_number"),
    col("author"),
    col("title"),
    col("estimated_outputs"),
    col("merged_at")
  )
  .orderBy(col("merged_at").desc)

outputsPerPullRequest.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_outputs_por_pr"
)

// COMMAND ----------

// DBTITLE 1,Total AI outputs per month compute and optional persist
val monthlyOutputTotals = pullRequestsWithEstimatedOutputs
  .groupBy(date_trunc("month", col("merged_at")).alias("month"))
  .agg(
    countDistinct("pr_number").alias("total_prs"),
    sum("estimated_outputs").alias("total_outputs"),
    round(avg("estimated_outputs"), 1).alias("avg_outputs_per_pr"),
    countDistinct("author").alias("unique_authors")
  )
  .orderBy(col("month").desc)

// monthlyOutputs.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable("usr.br__customer_lifecycle_optimization.ai_monitor_outputs_mensais")

// COMMAND ----------

// DBTITLE 1,Cumulative outputs and persist
val cumulativeOutputTotals = pullRequestsWithEstimatedOutputs
  .agg(
    sum("estimated_outputs").alias("total_outputs_to_date"),
    countDistinct("pr_number").alias("total_prs_to_date"),
    countDistinct("author").alias("unique_authors_to_date")
  )

cumulativeOutputTotals.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_total_acumulado"
)

// COMMAND ----------

// DBTITLE 1,PR content breakdown from description and persist
val pullRequestContentBreakdown = pullRequestsWithEstimatedOutputs
  .withColumn("attribute_type",
    regexp_extract(col("description"), "\\*\\*Type\\*\\*:\\s*([^\\n]+)", 1))
  .withColumn("owner_squad",
    regexp_extract(col("description"), "\\*\\*Owner\\*\\*:\\s*(?:Squad\\.)?([^\\n]+)", 1))
  .withColumn("domain",
    regexp_extract(col("description"), "\\*\\*Domain\\*\\*:\\s*([^\\n]+)", 1))
  .withColumn("context_summary",
    regexp_extract(col("description"), "###\\s*Context\\n+([^#\\n][^\\n]{0,200})", 1))
  .withColumn("output_description",
    regexp_extract(col("description"), "\\*\\*Output[:\\*]*\\s*([^\\n]{0,200})", 1))
  .withColumn("warp_pipe_product",
    regexp_extract(col("title"), "(?i)Add\\s+(.+?)\\s+(?:Activity|JM|Journey)", 1))
  .withColumn("month", date_format(col("merged_at"), "yyyy-MM"))
  .select(
    col("month"),
    col("ai_tool"),
    col("pr_number"),
    col("author"),
    col("title"),
    col("attribute_type"),
    col("owner_squad"),
    col("domain"),
    col("context_summary"),
    col("output_description"),
    col("warp_pipe_product"),
    col("estimated_outputs"),
    col("merged_at")
  )
  .orderBy(col("merged_at").desc)

pullRequestContentBreakdown.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable(
  "usr.br__customer_lifecycle_optimization.ai_monitor_pr_content"
)

