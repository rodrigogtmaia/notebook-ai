// DBTITLE 1,Load pull request events base
// Base dataset: one row per PR event; analysis restricts to merged closes only.
val pullRequestEventsTableName = "etl.ist__dataset.pull_request_events"
val pullRequestEvents = spark.table(pullRequestEventsTableName)

// COMMAND ----------

// MAGIC %md
// MAGIC ## 3. Widgets

// COMMAND ----------

// MAGIC %md
// MAGIC No runtime widgets. Scope is fixed for this pipeline (`merged_at` from 2025-01-01 onward per OKR definition).

