// DBTITLE 1,Check required columns after read
val requiredColumns = Seq(
  "action",
  "is_merged",
  "merged_at",
  "repo_name",
  "description",
  "title",
  "pr_number",
  "author",
  "added_lines",
  "deleted_lines",
  "changed_files",
  "created_at"
)
val missingRequiredColumns = requiredColumns.filterNot(pullRequestEvents.columns.contains)
println(s"Required columns: ${requiredColumns.mkString(", ")}")
require(
  missingRequiredColumns.isEmpty,
  s"Missing required columns on $pullRequestEventsTable: ${missingRequiredColumns.mkString(", ")}"
)

// COMMAND ----------

// DBTITLE 1,Validate filter impact — merged cohort vs AI subset at PR grain
val distinctPrMergedCohort = mergedClosedPullRequests.select("repo_name", "pr_number").distinct().count()
val distinctPrAiSubset = aiAssistedPullRequests.select("repo_name", "pr_number").distinct().count()
println(s"Distinct (repo_name, pr_number) after closed + merged + merged_at >= $mergedSince: $distinctPrMergedCohort")
println(s"Distinct (repo_name, pr_number) after AI classification filters: $distinctPrAiSubset")
println("Expected: AI subset count <= merged cohort count.")

// COMMAND ----------

// DBTITLE 1,Check duplicates at PR grain in AI population
val duplicatePrGroups = aiAssistedPullRequests
  .groupBy("repo_name", "pr_number")
  .count()
  .filter(col("count") > 1)

val duplicatePrGroupCount = duplicatePrGroups.count()
println(s"PR groups with more than one row in aiAssistedPullRequests: $duplicatePrGroupCount")
println("Expected: 0 for a clean PR grain; if > 0, sum() on lines/files double-counts unless deduplicated.")
display(duplicatePrGroups.limit(50))

// COMMAND ----------

// DBTITLE 1,Min and max merged_at after AI filters
display(
  aiAssistedPullRequests.agg(
    min(col("merged_at")).alias("min_merged_at"),
    max(col("merged_at")).alias("max_merged_at")
  )
)
println(s"Expected: min merged_at >= $mergedSince (string cutoff applied to timestamp column — verify type semantics in production).")

