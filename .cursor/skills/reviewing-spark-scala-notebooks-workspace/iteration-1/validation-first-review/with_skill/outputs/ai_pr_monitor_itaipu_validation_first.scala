// MAGIC ## 8. Display

// COMMAND ----------

// DBTITLE 1,Write and display monthly volume
monthlyVolume.write.format("delta").mode("overwrite").option("overwriteSchema", "true").saveAsTable("usr.br__customer_lifecycle_optimization.ai_monitor_volume_mensal")
display(monthlyVolume)

// COMMAND ----------

