// MAGIC
// MAGIC **Context**: Monitor merged pull requests that used AI-assisted workflows in the `itaipu` (Rapidash / Warp Pipe) and `cross-segment-ai-tools` repositories.
// MAGIC
// MAGIC **Objective**: Support **monthly OKR reporting** for AI-assisted PRs (volume, authors, time-to-merge, estimated outputs, and content fields extracted from descriptions).
// MAGIC
// MAGIC **Method**:
// MAGIC 1. Read closed, merged PR events from `etl.ist__dataset.pull_request_events` from a configurable merge-date cutoff.
// MAGIC 2. Classify PRs into `ai_tool` (Rapidash, Warp Pipe, cross-segment-ai-tools) using repository and description/title rules preserved from the original notebook.
// MAGIC 3. Build monthly and author-level aggregates, PR listings, estimated output counts, cumulative totals, and Rapidash/Warp Pipe content extracts.
// MAGIC 4. Materialize results to Delta tables under `usr.br__customer_lifecycle_optimization` and validate grain and schema assumptions.
// MAGIC
// MAGIC **Analysis structure**:
// MAGIC - 0. Intro / README (this block)
// MAGIC - 1. Imports
// MAGIC - 2. Data sources
// MAGIC - 3. Widgets
// MAGIC - 4. Variable filters
// MAGIC - 5. Functions
// MAGIC - 6. Code / analysis
// MAGIC - 7. Validation
// MAGIC - 8. Display
// MAGIC
// MAGIC **Data sources**:
// MAGIC - `etl.ist__dataset.pull_request_events` — closed, merged PR events; grain is one row per event; keys used: `repo_name`, `pr_number`, `author`, `merged_at`, `created_at`, description/title fields, line and file stats.
// MAGIC
// MAGIC **Assumptions / caveats**:
// MAGIC - Rapidash PRs are identified by `repo_name === "itaipu"` and `description` containing `Generated by Rapidash`.
// MAGIC - Warp Pipe PRs use additional title/description heuristics; infra-only mentions are excluded by design.
// MAGIC - `cross-segment-ai-tools` PRs are included in full and tagged as that tool.
// MAGIC - Estimated AI outputs use description parsing heuristics documented in the analysis section; counts are approximate.

