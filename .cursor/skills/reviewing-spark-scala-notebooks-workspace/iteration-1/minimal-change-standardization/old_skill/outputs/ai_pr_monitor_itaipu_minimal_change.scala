// COMMAND ----------

// DBTITLE 1,Load pull request events
val pullRequestEvents = spark.table("etl.ist__dataset.pull_request_events")

// COMMAND ----------

// DBTITLE 1,Check required columns after read
val requiredPrEventColumns = Seq(
  "action",
  "is_merged",
  "merged_at",
  "created_at",
  "repo_name",
  "description",
  "title",
  "pr_number",
  "author",
  "added_lines",
  "deleted_lines",
  "changed_files"
)
val missingPrEventColumns = requiredPrEventColumns.filterNot(pullRequestEvents.columns.contains)
println(s"Expected: no missing columns from $requiredPrEventColumns")
require(missingPrEventColumns.isEmpty, s"Missing columns: ${missingPrEventColumns.mkString(", ")}")

// COMMAND ----------

// MAGIC %md
// MAGIC ## 3. Widgets

// COMMAND ----------

// DBTITLE 1,Merge window lower bound widget
dbutils.widgets.text("mergedAtSince", "2025-01-01")

// COMMAND ----------

// MAGIC %md
// MAGIC ## 4. Variable filters

// COMMAND ----------

// DBTITLE 1,Filter closed merged PRs from cutoff date
val mergedAtSince = dbutils.widgets.get("mergedAtSince")

val allPRs = pullRequestEvents
  .filter(col("action") === "closed")
  .filter(col("is_merged") === true)
  .filter(col("merged_at") >= mergedAtSince)

// COMMAND ----------

// DBTITLE 1,Define Rapidash and Warp Pipe classification filters
// Rapidash: explicit signature "Generated by Rapidash" in the description
val rapidashFilter =
  col("repo_name") === "itaipu" &&
  col("description").like("%Generated by Rapidash%")

// Warp Pipe: PRs that effectively create products (JM + Activity + Eligibility + Conversion)
// Excludes infra/cleanup PRs that only mention Warp Pipe
val warpPipeFilter =
  col("repo_name") === "itaipu" && (
    col("description").like("%Warp Pipe%") ||
    col("description").like("%WarpPipe%")
  ) && (
    col("title").like("%w/ Warp Pipe%") ||
    col("description").like("%Config object%") ||
    col("description").like("%productCustomerStatus%")
  )

// COMMAND ----------

// DBTITLE 1,Build AI-assisted PR population with ai_tool label
val aiPRs = allPRs
  .filter(rapidashFilter || warpPipeFilter || col("repo_name") === "cross-segment-ai-tools")
  .withColumn("ai_tool",
    when(col("repo_name") === "cross-segment-ai-tools", lit("cross-segment-ai-tools"))
    .when(col("description").like("%Generated by Rapidash%"), lit("Rapidash"))
    .otherwise(lit("Warp Pipe"))
  )

// COMMAND ----------

