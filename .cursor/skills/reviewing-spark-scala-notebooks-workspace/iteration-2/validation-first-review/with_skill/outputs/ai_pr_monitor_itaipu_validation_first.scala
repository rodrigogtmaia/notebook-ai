// MAGIC
// MAGIC **Context**: This notebook supports monthly OKR reporting on AI-assisted pull request activity in engineering repositories.
// MAGIC
// MAGIC **Objective**: Measure monthly AI-assisted PR volume, author activity, time to merge, estimated outputs, and content breakdown for reporting.
// MAGIC
// MAGIC **Method**:
// MAGIC 1. Read merged closed PR events from a single source table (no joins).
// MAGIC 2. Classify PRs by tool (Rapidash, Warp Pipe, cross-segment-ai-tools) using preserved filter logic.
// MAGIC 3. Build monthly and cumulative aggregates and Delta output tables.
// MAGIC 4. Validate required columns, date scope, repo scope, PR grain, duplicate-row risk, and aggregation consistency.
// MAGIC 5. Display the same result sets as the operational notebook.
// MAGIC
// MAGIC **Analysis structure**:
// MAGIC - 0. Intro / README (this block)
// MAGIC - 1. Imports
// MAGIC - 2. Data sources
// MAGIC - 3. Widgets
// MAGIC - 4. Variable filters
// MAGIC - 5. Functions
// MAGIC - 6. Code / analysis
// MAGIC - 7. Validation
// MAGIC - 8. Display
// MAGIC
// MAGIC **Data sources**:
// MAGIC - `etl.ist__dataset.pull_request_events` — single source of PR events; grain is one row per event row in the table (validated against PR key); no joins in this pipeline.
// MAGIC
// MAGIC **Main assumptions**:
// MAGIC - `countDistinct("pr_number")` is the intended PR cardinality metric; sums of line/file metrics assume at most one analytic row per `(repo_name, pr_number)` in `aiPRs` (validated below).
// MAGIC - Lower bound `merged_at >= "2025-01-01"` and repository filters are fixed scope for this OKR slice.

