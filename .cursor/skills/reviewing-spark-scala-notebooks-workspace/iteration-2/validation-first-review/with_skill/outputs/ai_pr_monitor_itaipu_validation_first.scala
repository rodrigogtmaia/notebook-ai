// MAGIC %md
// MAGIC ## 7. Validation

// COMMAND ----------

// MAGIC %md
// MAGIC ### No joins in this pipeline
// MAGIC This analysis reads **only** `etl.ist__dataset.pull_request_events`. There are **no joins**. Duplicate and fan-out risk therefore does **not** come from join cardinality; it comes from **multiple source rows for the same PR** (same `repo_name` + `pr_number`). The checks below validate filters, grain, and aggregation safety under that model.

// COMMAND ----------

// DBTITLE 1,Check required columns after read
val requiredColumns = Seq(
  "action",
  "is_merged",
  "merged_at",
  "repo_name",
  "description",
  "title",
  "pr_number",
  "author",
  "added_lines",
  "deleted_lines",
  "changed_files",
  "created_at"
)
val missingAfterRead = requiredColumns.filterNot(allPRs.columns.contains)
require(
  missingAfterRead.isEmpty,
  s"Missing required columns on merged-closed slice: ${missingAfterRead.mkString(", ")}"
)

// COMMAND ----------

// DBTITLE 1,Validate date filter scope on merged_at
val mergedAtBoundsAll = allPRs.agg(
  min(col("merged_at")).alias("min_merged_at"),
  max(col("merged_at")).alias("max_merged_at")
).collect()(0)
println(
  s"[Validation] merged_at range after closed+merged+lower-bound filter: " +
    s"min=${mergedAtBoundsAll.getAs[Any]("min_merged_at")}, max=${mergedAtBoundsAll.getAs[Any]("max_merged_at")}"
)
// Lower bound is fixed in filters; min merged_at should be >= 2025-01-01 if data exists
val minTs = mergedAtBoundsAll.getAs[java.sql.Timestamp]("min_merged_at")
if (minTs != null) {
  require(
    !minTs.before(java.sql.Timestamp.valueOf("2025-01-01 00:00:00")),
    "merged_at min is before the configured lower bound 2025-01-01 — check date filter or data."
  )
}

// COMMAND ----------

// DBTITLE 1,Validate repo scope in classified PRs
val distinctRepos = aiPRs.select("repo_name").distinct().collect().map(_.getString(0)).sorted
println(s"[Validation] distinct repo_name in aiPRs: ${distinctRepos.mkString(", ")}")
val allowedRepos = Set("itaipu", "cross-segment-ai-tools")
val unexpected = distinctRepos.filterNot(allowedRepos.contains)
require(
  unexpected.isEmpty,
  s"Unexpected repo_name values in aiPRs (expected subset of itaipu, cross-segment-ai-tools): ${unexpected.mkString(", ")}"
)

// COMMAND ----------

// DBTITLE 1,Validate intended grain and duplicate PR rows
// Intended analytic grain for PR counts: one logical PR = (repo_name, pr_number)
val aiPrRowCount = aiPRs.count()
val distinctPrKeys = aiPRs.select("repo_name", "pr_number").distinct().count()
val distinctPrNumberOnly = aiPRs.select("pr_number").distinct().count()
println(s"[Validation] aiPRs row count: $aiPrRowCount")
println(s"[Validation] distinct (repo_name, pr_number): $distinctPrKeys")
println(s"[Validation] distinct pr_number only: $distinctPrNumberOnly")
require(
  aiPrRowCount == distinctPrKeys,
  s"Duplicate rows at PR grain (repo_name, pr_number): rows=$aiPrRowCount distinctKeys=$distinctPrKeys — sum() metrics may double-count; investigate source grain."
)
if (distinctPrNumberOnly < distinctPrKeys) {
  println("[Validation] WARN: same pr_number appears under more than one repo_name in scope (unusual); PR metrics use countDistinct(pr_number) — interpret cross-repo collisions carefully.")
}

// COMMAND ----------

// DBTITLE 1,Validate aggregation safety for monthly PR counts
// For each month, sum of total_prs across ai_tool should match distinct PRs merged in that month
val monthlyFromVolume = monthlyVolume
  .groupBy("month")
  .agg(sum("total_prs").alias("sum_prs_by_tool"))
val distinctByMonth = aiPRs
  .withColumn("month", date_trunc("month", col("merged_at")))
  .groupBy("month")
  .agg(countDistinct("pr_number").alias("distinct_prs_month"))
val monthlyRecon = monthlyFromVolume.join(distinctByMonth, Seq("month"), "full_outer")
  .withColumn("delta", col("sum_prs_by_tool") - col("distinct_prs_month"))
display(monthlyRecon.select("month", "sum_prs_by_tool", "distinct_prs_month", "delta"))
val reconOff = monthlyRecon.filter(
  col("sum_prs_by_tool").isNull ||
    col("distinct_prs_month").isNull ||
    col("delta").isNull ||
    col("delta") =!= lit(0)
).count()
require(reconOff == 0, "Monthly volume total_prs does not reconcile to countDistinct(pr_number) by month — check classification or duplicates.")

// COMMAND ----------

// DBTITLE 1,Validate sum metrics use same row set as PR grain
// If duplicate PR rows were present, line sums would inflate while countDistinct would not; covered by PR-key duplicate check above.
println("[Validation] Aggregation safety: PR cardinality uses countDistinct(pr_number); line/file sums use all aiPRs rows — safe only if one row per (repo_name, pr_number) (asserted above).")

