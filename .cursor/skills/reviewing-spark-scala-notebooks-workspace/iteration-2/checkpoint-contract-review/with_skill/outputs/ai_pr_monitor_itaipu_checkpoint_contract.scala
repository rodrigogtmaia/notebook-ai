// MAGIC - 3. Widgets
// MAGIC - 4. Variable filters
// MAGIC - 5. Functions
// MAGIC - 6. Code / analysis
// MAGIC - 7. Validation
// MAGIC - 8. Display
// MAGIC
// MAGIC **Data sources**:
// MAGIC - `etl.ist__dataset.pull_request_events` — single source for PR events, filters, and content fields used in metrics.
// MAGIC
// MAGIC **Main assumptions**:
// MAGIC - `action = closed` and `is_merged = true` define the merged PR population.
// MAGIC - PR identity is unique on `(repo_name, pr_number)`; duplicate rows at that grain would inflate sums that use `sum(...)` across PR-level attributes.
// MAGIC - Merge timestamps and string filters behave as in the original notebook (including `merged_at >= mergeStartDate` as a string comparison where applicable).

