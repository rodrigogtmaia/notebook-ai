// MAGIC 3. Build monthly and PR-level metrics, including estimated AI outputs from description heuristics.
// MAGIC 4. Run validations for required columns, PR-grain duplicates, and aggregation safety before relying on sums at PR level.
// MAGIC 5. Write results to Delta tables under `usr.br__customer_lifecycle_optimization` and display key outputs.
// MAGIC
// MAGIC **Analysis structure**:
// MAGIC - 0. Intro / README (this block)
// MAGIC - 1. Imports
