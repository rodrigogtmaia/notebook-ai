// MAGIC ## 7. Validation

// COMMAND ----------

// DBTITLE 1,Check required columns after scope filters
val requiredColumns = Seq(
  "action",
  "is_merged",
  "merged_at",
  "repo_name",
  "description",
  "title",
  "pr_number",
  "author",
  "added_lines",
  "deleted_lines",
  "changed_files",
  "created_at"
)

val missingColumns = requiredColumns.filterNot(allPRs.columns.contains)
require(
  missingColumns.isEmpty,
  s"Missing required columns on scoped PR events: ${missingColumns.mkString(", ")}"
)

// COMMAND ----------

// DBTITLE 1,Check duplicates at pull-request grain (repo_name pr_number)
val duplicatePrGrain = aiPRs
  .groupBy("repo_name", "pr_number")
  .count()
  .filter(col("count") > 1)

val duplicatePrGrainCount = duplicatePrGrain.count()
println(s"Duplicate (repo_name, pr_number) groups in aiPRs: $duplicatePrGrainCount")
display(duplicatePrGrain.orderBy(col("count").desc).limit(100))

// COMMAND ----------

// DBTITLE 1,Validate aggregation safety for PR-level sums
val totalAiRows = aiPRs.count()
val distinctPrKeys = aiPRs.select("repo_name", "pr_number").distinct().count()

println(s"aiPRs row count: $totalAiRows")
println(s"Distinct (repo_name, pr_number): $distinctPrKeys")

if (totalAiRows != distinctPrKeys) {
  println(
    "WARNING: Row count exceeds distinct PR keys. sum(added_lines), sum(deleted_lines), sum(changed_files), and sum(estimated_outputs) may double-count PR-level metrics unless each source row represents a distinct PR event you intend to sum."
  )
}

val sumAddedAllRows = aiPRs
  .agg(coalesce(sum("added_lines"), lit(0)).cast("long").alias("s"))
  .first()
  .getLong(0)

val sumAddedDedupedByPr = aiPRs
  .groupBy("repo_name", "pr_number")
  .agg(coalesce(max("added_lines"), lit(0)).cast("long").alias("added_lines_max"))
  .agg(coalesce(sum("added_lines_max"), lit(0L)).alias("s"))
  .first()
  .getLong(0)

println(s"Sum added_lines across all aiPRs rows: $sumAddedAllRows")
println(s"Sum of max(added_lines) per (repo_name, pr_number): $sumAddedDedupedByPr")

if (sumAddedAllRows != sumAddedDedupedByPr) {
  println(
    "WARNING: PR-grain deduped sum of added_lines differs from raw row sum. Review whether the event grain matches the intended OKR definition before interpreting line-churn totals."
  )
}

