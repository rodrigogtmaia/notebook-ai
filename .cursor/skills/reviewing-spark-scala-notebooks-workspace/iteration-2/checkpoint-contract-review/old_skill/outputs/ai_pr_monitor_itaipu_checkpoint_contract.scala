// COMMAND ----------

// DBTITLE 1,Load pull request events
val pullRequestEvents = spark.table("etl.ist__dataset.pull_request_events")

// COMMAND ----------

// DBTITLE 1,Check required columns after read
val requiredPullRequestColumns = Seq(
  "action",
  "is_merged",
  "merged_at",
  "repo_name",
  "description",
  "title",
  "pr_number",
  "author",
  "added_lines",
  "deleted_lines",
  "changed_files",
  "created_at"
)

val missingPullRequestColumns =
  requiredPullRequestColumns.filterNot(pullRequestEvents.columns.contains)

println(s"Expected: zero missing required columns -> $requiredPullRequestColumns")
require(
  missingPullRequestColumns.isEmpty,
  s"Missing columns: ${missingPullRequestColumns.mkString(", ")}"
