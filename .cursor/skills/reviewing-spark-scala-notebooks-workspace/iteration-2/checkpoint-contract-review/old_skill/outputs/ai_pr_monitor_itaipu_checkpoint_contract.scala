// DBTITLE 1,Build AI-assisted PR population
def buildAiAssistedPullRequests(events: DataFrame, mergedSinceCutoff: String): DataFrame = {
  val allPRs = events
    .filter(col("action") === "closed")
    .filter(col("is_merged") === true)
    .filter(col("merged_at") >= mergedSinceCutoff)

  val rapidashFilter =
    col("repo_name") === "itaipu" &&
      col("description").like("%Generated by Rapidash%")

  val warpPipeFilter =
    col("repo_name") === "itaipu" && (
      col("description").like("%Warp Pipe%") ||
        col("description").like("%WarpPipe%")
    ) && (
      col("title").like("%w/ Warp Pipe%") ||
        col("description").like("%Config object%") ||
        col("description").like("%productCustomerStatus%")
    )

  allPRs
    .filter(rapidashFilter || warpPipeFilter || col("repo_name") === "cross-segment-ai-tools")
    .withColumn(
      "ai_tool",
      when(col("repo_name") === "cross-segment-ai-tools", lit("cross-segment-ai-tools"))
        .when(col("description").like("%Generated by Rapidash%"), lit("Rapidash"))
        .otherwise(lit("Warp Pipe"))
    )
}

// COMMAND ----------

// MAGIC %md
// MAGIC ## 6. Code / analysis

// COMMAND ----------

// DBTITLE 1,Apply AI PR classification
val aiPRs = buildAiAssistedPullRequests(pullRequestEvents, mergedSince)

// COMMAND ----------

// DBTITLE 1,Estimated outputs and Rapidash parsing helpers
val typeCount = size(split(col("description"), "\\*\\*Type\\*\\*:")) - 1

val bulletCount =
  size(expr("filter(split(description, '\n'), x -> x rlike '^- \\\\*\\\\*[A-Z][a-zA-Z0-9]*\\\\*\\\\*')"))

val attributeCount = greatest(typeCount, bulletCount)

val withOutputs = aiPRs
  .withColumn(
    "estimated_outputs",
    when(col("ai_tool") === "cross-segment-ai-tools", lit(1))
      .when(col("ai_tool") === "Warp Pipe", lit(1))
      .when(attributeCount > 0, attributeCount)
      .otherwise(greatest(lit(1), col("changed_files") - 2))
  )

// COMMAND ----------

// DBTITLE 1,Monthly PR volume
val monthlyVolume = aiPRs
  .groupBy(date_trunc("month", col("merged_at")).alias("month"), col("ai_tool"))
  .agg(
    countDistinct("pr_number").alias("total_prs"),
    countDistinct("author").alias("unique_authors"),
    sum("added_lines").alias("total_lines_added"),
    sum("deleted_lines").alias("total_lines_deleted"),
    sum("changed_files").alias("total_files_changed")
  )
  .orderBy(col("month").desc, col("ai_tool"))

// COMMAND ----------

// DBTITLE 1,Breakdown by author and month
val byAuthor = aiPRs
  .groupBy(
    date_trunc("month", col("merged_at")).alias("month"),
    col("ai_tool"),
    col("author")
  )
  .agg(
    countDistinct("pr_number").alias("prs_merged"),
    sum("added_lines").alias("lines_added"),
    sum("deleted_lines").alias("lines_deleted")
  )
  .orderBy(col("month").desc, col("prs_merged").desc)

// COMMAND ----------

// DBTITLE 1,Average time to merge
val timeToMerge = aiPRs
  .withColumn(
    "hours_to_merge",
    round((unix_timestamp(col("merged_at")) - unix_timestamp(col("created_at"))) / 3600, 1)
  )
  .groupBy(date_trunc("month", col("merged_at")).alias("month"), col("ai_tool"))
  .agg(
    round(avg("hours_to_merge"), 1).alias("avg_hours_to_merge"),
    min("hours_to_merge").alias("min_hours"),
    max("hours_to_merge").alias("max_hours"),
    countDistinct("pr_number").alias("total_prs")
  )
  .orderBy(col("month").desc, col("ai_tool"))

// COMMAND ----------

// DBTITLE 1,Individual PR list
val prList = aiPRs
  .withColumn("month", date_format(col("merged_at"), "yyyy-MM"))
  .withColumn(
    "hours_to_merge",
    round((unix_timestamp(col("merged_at")) - unix_timestamp(col("created_at"))) / 3600, 0)
  )
  .select(
    col("month"),
    col("ai_tool"),
    col("repo_name"),
    col("pr_number"),
    col("author"),
    col("title"),
    col("added_lines"),
    col("deleted_lines"),
    col("changed_files"),
    col("hours_to_merge"),
    col("merged_at")
  )
  .orderBy(col("merged_at").desc)

// COMMAND ----------

// DBTITLE 1,AI outputs per PR
val outputsPerPR = withOutputs
  .withColumn("month", date_format(col("merged_at"), "yyyy-MM"))
  .select(
    col("month"),
    col("ai_tool"),
    col("pr_number"),
    col("author"),
    col("title"),
    col("estimated_outputs"),
    col("merged_at")
  )
  .orderBy(col("merged_at").desc)

// COMMAND ----------

// DBTITLE 1,Total AI outputs per month
val monthlyOutputs = withOutputs
  .groupBy(date_trunc("month", col("merged_at")).alias("month"))
  .agg(
    countDistinct("pr_number").alias("total_prs"),
    sum("estimated_outputs").alias("total_outputs"),
    round(avg("estimated_outputs"), 1).alias("avg_outputs_per_pr"),
    countDistinct("author").alias("unique_authors")
  )
  .orderBy(col("month").desc)

// COMMAND ----------

// DBTITLE 1,Cumulative total of AI-generated outputs
val cumulativeTotal = withOutputs
  .agg(
    sum("estimated_outputs").alias("total_outputs_to_date"),
    countDistinct("pr_number").alias("total_prs_to_date"),
    countDistinct("author").alias("unique_authors_to_date")
  )

// COMMAND ----------

// DBTITLE 1,PR content breakdown from description
val prContent = withOutputs
  .withColumn("attribute_type",
    regexp_extract(col("description"), "\\*\\*Type\\*\\*:\\s*([^\\n]+)", 1))
  .withColumn("owner_squad",
    regexp_extract(col("description"), "\\*\\*Owner\\*\\*:\\s*(?:Squad\\.)?([^\\n]+)", 1))
  .withColumn("domain",
    regexp_extract(col("description"), "\\*\\*Domain\\*\\*:\\s*([^\\n]+)", 1))
  .withColumn("context_summary",
    regexp_extract(col("description"), "###\\s*Context\\n+([^#\\n][^\\n]{0,200})", 1))
  .withColumn("output_description",
    regexp_extract(col("description"), "\\*\\*Output[:\\*]*\\s*([^\\n]{0,200})", 1))
  .withColumn("warp_pipe_product",
    regexp_extract(col("title"), "(?i)Add\\s+(.+?)\\s+(?:Activity|JM|Journey)", 1))
  .withColumn("month", date_format(col("merged_at"), "yyyy-MM"))
  .select(
    col("month"),
    col("ai_tool"),
    col("pr_number"),
    col("author"),
    col("title"),
    col("attribute_type"),
    col("owner_squad"),
    col("domain"),
    col("context_summary"),
    col("output_description"),
    col("warp_pipe_product"),
    col("estimated_outputs"),
    col("merged_at")
  )
  .orderBy(col("merged_at").desc)

// COMMAND ----------

// MAGIC %md
// MAGIC ## 7. Validation

// COMMAND ----------

// DBTITLE 1,Check duplicate rows at repo and PR grain
val duplicateRepoPrKeys = aiPRs
  .groupBy("repo_name", "pr_number")
  .count()
  .filter(col("count") > 1)

val duplicateRepoPrKeyCount = duplicateRepoPrKeys.count()
println(s"Expected: zero duplicate keys at (repo_name, pr_number) grain -> found $duplicateRepoPrKeyCount")
require(duplicateRepoPrKeyCount == 0L, "Unexpected duplicate rows at repo_name + pr_number grain")

// COMMAND ----------

// DBTITLE 1,Compare pr_number-only vs composite distinct counts
val distinctPrNumberOnly = aiPRs.select("pr_number").distinct().count()
val distinctRepoAndPr = aiPRs.select("repo_name", "pr_number").distinct().count()

println(s"Distinct pr_number: $distinctPrNumberOnly")
println(s"Distinct (repo_name, pr_number): $distinctRepoAndPr")
println(
  "Expected: if these differ, pr_number is reused across repos and countDistinct(pr_number) metrics may be misleading"
)

// COMMAND ----------

// DBTITLE 1,Reconcile monthly volume with distinct PR population
val distinctAiPrCount = aiPRs.select("repo_name", "pr_number").distinct().count()
val sumMonthlyTotalPrs = monthlyVolume.agg(sum("total_prs").cast("long")).first().getLong(0)

println(s"Distinct PRs in aiPRs (repo + number): $distinctAiPrCount")
println(s"Sum of monthly total_prs across (month, ai_tool): $sumMonthlyTotalPrs")
println(
  "Expected: sums match when each PR falls in exactly one month and one ai_tool bucket (OKR safety check)"
)
require(
  distinctAiPrCount == sumMonthlyTotalPrs,
  s"Aggregation safety: distinct PRs $distinctAiPrCount != sum monthly total_prs $sumMonthlyTotalPrs"
)

// COMMAND ----------

// DBTITLE 1,Reconcile author breakdown totals per month and tool
val reconciledByAuthor = byAuthor
  .groupBy("month", "ai_tool")
  .agg(sum("prs_merged").cast("long").alias("sum_author_bucket_prs"))

val monthlyForJoin = monthlyVolume.select(
  col("month"),
  col("ai_tool"),
  col("total_prs").alias("monthly_total_prs")
)

val authorReconciliation = monthlyForJoin.join(reconciledByAuthor, Seq("month", "ai_tool"), "inner")

val authorMismatchCount = authorReconciliation
  .filter(col("monthly_total_prs") =!= col("sum_author_bucket_prs"))
  .count()

println(
  s"Expected: zero month and ai_tool groups where sum of author PR counts != monthly total_prs -> mismatches: $authorMismatchCount"
)
require(authorMismatchCount == 0L, "Author-level PR counts do not reconcile to monthly volume")

