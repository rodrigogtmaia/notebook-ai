// MAGIC - `etl.ist__dataset.pull_request_events` — event-level pull request records; grain is one row per event; this pipeline uses merged, closed PR rows and treats each `(repo_name, pr_number)` as one logical PR after filters.
// MAGIC
// MAGIC **Assumptions**:
// MAGIC - `merged_at` on or after the widget cutoff is the reporting window for merges.
