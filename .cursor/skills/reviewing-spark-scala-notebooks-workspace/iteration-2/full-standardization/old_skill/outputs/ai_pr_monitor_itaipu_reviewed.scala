// DBTITLE 1,Load pull request events base
val pullRequestEvents: DataFrame = spark.table("etl.ist__dataset.pull_request_events")

// COMMAND ----------

// MAGIC %md
// MAGIC ## 3. Widgets

// COMMAND ----------

// DBTITLE 1,Create merged-at cutoff widget
dbutils.widgets.text("mergedAtCutoff", "2025-01-01")

