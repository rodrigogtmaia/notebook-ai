// MAGIC ## 5. Functions

// COMMAND ----------

// DBTITLE 1,Heuristics for estimated Rapidash outputs
def typeDeclarationCount(descriptionColumn: org.apache.spark.sql.Column): org.apache.spark.sql.Column =
  size(split(descriptionColumn, "\\*\\*Type\\*\\*:")) - 1

def attributeBulletCount(descriptionColumn: org.apache.spark.sql.Column): org.apache.spark.sql.Column =
  size(expr("filter(split(description, '\n'), x -> x rlike '^- \\\\*\\\\*[A-Z][a-zA-Z0-9]*\\\\*\\\\*')"))

def withEstimatedOutputs(baseAiPrs: DataFrame): DataFrame = {
  val typeCnt = typeDeclarationCount(col("description"))
  val bulletCnt = attributeBulletCount(col("description"))
  val attributeCnt = greatest(typeCnt, bulletCnt)

  baseAiPrs
    .withColumn(
      "estimated_outputs",
      when(col("ai_tool") === "cross-segment-ai-tools", lit(1))
        .when(col("ai_tool") === "Warp Pipe", lit(1))
        .when(attributeCnt > 0, attributeCnt)
        .otherwise(greatest(lit(1), col("changed_files") - 2))
    )
}

